class Persona():
#atributos
 nombre = ""
 apellido=""
 genero=""

#metodos getter y setter
def getNombre(self):
    return self.nombre

def setNombre(self, nombre):
    self.nombre = nombre

def getApellido(self):
    return self.apellido

def setApellido(self, apellido):
    self.apellido = apellido

def getGenero(self):
    return self.genero

def setGenero(self, genero):
    self.genero = genero



